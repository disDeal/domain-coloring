pub fn get_hot_color(x: f64) -> Color {
    let x = x.max(0.).min(1.);

    let r = Color::new(1.0, 0.0, 0.0);
    let g = Color::new(0.0, 1.0, 0.0);
    let b = Color::new(0.0, 0.0, 1.0);

    if x < 0.4 {
        let t = x / 0.4;
        t * r
    } else if x < 0.8 {
        let t = (x - 0.4) / (0.8 - 0.4);
        r + t * g
    } else {
        let t = (x - 0.8) / (1.0 - 0.8);
        r + g + t * b
    }
}

pub fn get_gray_color(x: f64) -> Color {
    let x = x.max(0.).min(1.);

    (1.0 - x) * Color::new(1.0, 1.0, 1.0)
}


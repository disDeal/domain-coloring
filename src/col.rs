pub enum ColormapType {
    Heat,
    Jet,
    Magma,
    Inferno,
    Plasma,
    Viridis,
    Cividis,
    Github,
}

#[derive(Copy, Clone)]
pub struct Color {
    pub data: [f64; 3],
}

impl Color {
    fn new(r: f64, g: f64, b: f64) -> Color {
        Color { data: [r, g, b] }
    }

    pub fn r(&self) -> u8 {
        (self.data[0] * 255.) as u8
    }
    pub fn g(&self) -> u8 {
        (self.data[1] * 255.) as u8
    }
    pub fn b(&self) -> u8 {
        (self.data[2] * 255.) as u8
    }
}

impl std::ops::Add<Color> for Color {
    type Output = Color;
    fn add(self, other: Color) -> Color {
        Color {
            data: [
                self.data[0] + other.data[0],
                self.data[1] + other.data[1],
                self.data[2] + other.data[2],
            ],
        }
    }
}

impl std::ops::Mul<f64> for Color {
    type Output = Color;

    fn mul(self, other: f64) -> Color {
        Color {
            data: [
                self.data[0] * other,
                self.data[1] * other,
                self.data[2] * other,
            ],
        }
    }
}

impl std::ops::Mul<Color> for f64 {
    type Output = Color;

    fn mul(self, other: Color) -> Color {
        Color {
            data: [
                self * other.data[0],
                self * other.data[1],
                self * other.data[0],
            ],
        }
    }
}

